 
//Data types present in JS
//auto casting available in JS
//ES6 - Ecmascript6, es2015

var myDataType = "Luke";
console.log("My datatype is: ", typeof myDataType); //String

myDataType = .12455;
console.log("My second datatype (no casting) is: ", typeof myDataType); //number

myDataType = true;
console.log("My third datatype (no casting) is: ", typeof myDataType); //boolean

myDataType = {};
console.log("My fourth datatype (no casting) is: ", typeof myDataType); //object

myDataType = null; //typeof null is object - interview question (null is valid datatype in JS)
console.log("My fifth datatype (no casting) is: ", typeof myDataType); //object

myDataType = undefined; // undefined
console.log("My fifth datatype (no casting) is: ", typeof myDataType); //undefined


